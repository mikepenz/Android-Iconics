    override val description: String
        get() = "7000+ Material Design Icons from the Community"

    override val license: String
        get() = "Pictogrammers Free License"

    override val licenseUrl: String
        get() = "https://github.com/Templarian/MaterialDesign/blob/master/LICENSE"

    override fun getIcon(key: String): IIcon {
        try {
            return Icon.valueOf(key)
        } catch (ex: Exception) {
            // ignore error, if not in 1st set, it has to be in the second
        }
        try {
            return Icon2.valueOf(key)
        } catch (ex: Exception) {
            // ignore error, if not in 1st set, it has to be in the second
        }
        return Icon3.valueOf(key)
    }

